#include <stdio.h>

// for loop

// step1 : Initialize- Setting a loop counter to an initial value
// step2: Test- checking a condition
// step3 : Increment/decrement -  Updating the loop counter

int main()
{
    int n = 4;

    for (int i = 1; i <= n; i++)
    {
        printf("%d\n", i);
    }

    return 0;
}